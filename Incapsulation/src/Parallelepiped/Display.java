package Parallelepiped;

public class Display {
	public void printArea(int area){
		System.out.println("Площадь стороны прямоугольного параллелепипеда: " + area);
	}
	public void printVolume(int volume){
		System.out.println("Объём прямоугольного параллелепипеда: " + volume);
	}
	public void print(int area, int volume){
		System.out.println("Площадь стороны прямоугольного параллелепипеда: " + area);
		System.out.println("Объём прямоугольного параллелепипеда: " + volume);
	}
}
